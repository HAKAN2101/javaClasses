package day3_Variables;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Hello Everyone");
        System.out.println("My name is Cybertek, I m from Virginia ");

        System.out.println("=========================");

        System.out.println("Hello Everyone\nMy name is Cybertek, I m from Virginia ");

        System.out.println("==========================");

        // \t:
        System.out.println("\tToday I learned Java");
        System.out.println("it was so much fun");

        System.out.println("=========================");


        // \\: \
        System.out.println("/");
        System.out.println("\\");

        System.out.println("   /\\");
        System.out.println("  /  \\");
        System.out.println(" /    \\");
        System.out.println("/      \\");
        System.out.println("--------");

        System.out.println("======================");

        System.out.println("My name is 'Muhtar'");
        System.out.println("My name is \'Muhtar\'");

        System.out.println("==============================");

        System.out.println("My favorite show is \"Game Of Thrones\"");
    }
}
